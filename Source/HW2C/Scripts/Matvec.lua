function distsqvec3(vec1, vec2)
	return ((vec1.x - vec2.x) * (vec1.x - vec2.x) + (vec1.y - vec2.y) * (vec1.y - vec2.y) + (vec1.z - vec2.z) * (vec1.z - vec2.z))
end

function distlessthan(vec1, vec2, dist)
	return (distsqvec3(vec1, vec2) < (dist * dist))
end


